def get_derivative_points(xy0, xy1):
    """
    For a given pair of (x,y) point tuples return the (x, f'(x,y)) point where f' denotes the slope at x0
    :param xy0: (x0, y0)
    :param xy1: (x1, y1)
    :return: (x0, f'(x,y))
    """
    x0, y0 = xy0
    x1, y1 = xy1
    return x0, (y0 - y1) / (x0 - x1)


def plot_tuples(xys):
    """
    For a given list of (x,y) tuples, plot the corresponding graph
    :param xys: list of (x,y) tuples
    """
    plt.figure(figsize=(14, 10))
    xs, ys = zip(*xys)
    plt.plot(xs, ys)
    ax = plt.gca()
    ax.set_ylim(ax.get_ylim())
    ax.xaxis.tick_top()
    plt.show()
